return {
  "neovim/nvim-lspconfig",
  event = { "BufReadPre", "BufNewFile" },
  config = function()
    local lspconfig = require("lspconfig")
    -- Lua Language Server
    lspconfig.lua_ls.setup({
      settings = {
        Lua = {
          diagnostics = {
            globals = { "vim" },
          },
        },
      },
    })

    -- Rust Analyser
    lspconfig.rust_analyzer.setup({
      settings = {
        standalone = true,
      },
    })
  end,
}
